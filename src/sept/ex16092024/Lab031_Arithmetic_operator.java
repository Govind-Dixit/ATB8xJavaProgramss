package sept.ex16092024;

public class Lab031_Arithmetic_operator {

    public static void main(String[] args) {
        // Arithmetic operator

        // +,-,/,*

        int a = 10 ;
        int b = 2 ;
        System.out.println(a+b); // + ->binary - need 2 operands
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % modulus -  Remainders
        System.out.println(a%b);

        System.out.println(67%13);



    }
}
